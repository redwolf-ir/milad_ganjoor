سید حسین رضوی معروف به غبار همدانی (زادهٔ ۱۲۷۰ یا ۱۲۶۵ قمری در همدان - درگذشتهٔ ۱۳۲۲ قمری) شاعر همدانی است. وی از دومان حاج سید صادق امام جمعهٔ همدان و از سادات کبابى آن دیار بود. وى در همدان قدم به عرصه هستى نهاد. علوم ادبى و عربى را در زاگاهش فراگرفت و با استعداد سرشار و هوش و ذکاوت فوق‏العاده‏اى که داشت در تحصیل دانش، پیشرفت چشمگیرى از خود نشان داد و در شمار استادان بنام شعر و ادب همدان قرار گرفت. غبار چندى در طریق عرفان قدم گذشت و به سیر و سلوک در این رهگذر عمر صرف کرد. دیوان شعر غبار با این که بیش از هزار بیت نیست، اما مکرر در همدان و اصفهان و تهران طبع و نشر گردیده است. وى در همدان درگذشت و پیکرش را به قم منتقل کرده در صحن حضرت معصومه (س) به خاک سپردند.
