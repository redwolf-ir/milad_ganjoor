<p>
سیف الدین ابوالمحامد محمد الفرغانی از شاعران عالیقدر نیمهٔ دوم قرن هفتم و اوایل قرن هشتم هجری است. وی بعد از خروج از زادگاه خود (فرغانه) مدتی در آذربایجان و  بلاد روم و آسیای صغیر به سر برده است. به طوری که از آثار او استنباط می‌شود وی اهل تصوف و عرفان بوده و سالها به کسب کمالات معنوی و سیر و سیاحت پرداخته است. مجموعه اشعار او از غزل و فصیده و قطعه و رباعی حدود ده الی یازده هزار بیت است. وی ارادتی وافر به سعدی داشته و بین آن دو مکاتباتی نیز بوده است. این شاعر بزرگوار در سال ۷۴۹ هجری در یکی از خانقاه های آقسرا وفات یافت. قصیدهٔ او که با مصرع «هم مرگ بر جهان شما نیز بگذرد» آغاز می‌شود و گویا خطاب به مغولان مهاجم سروده شده از اشعار معروف این شاعر آزاده است (<a href="https://ganjoor.net/seyf/divan-seyf/ghgh-seyf/sh12/">بخوانید</a>).
