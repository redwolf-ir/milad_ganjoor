سید محمدحسین بهجت تبریزی متخلص به شهریار، شاعر پارسی‌گوی آذری‌زبان، در سال ۱۲۸۵ هجری شمسی ر بازارچه میرزا نصراله تبریزی واقع در چای کنار چشم به جهان گشود. در سال ۱۳۲۸هجری قمری که تبریز آبستن حوادث خونین وقایع مشروطیت بود پدرش او را به روستای قیش قورشان  و خشکناب منتقل نمود. دورهٔ کودکی استاد در خاستگاه پدری و در آغوش طبیعت و روستا سپری شد که منظومه حیدربابا مولود آن خاطرات است. او تحصیلات خود را در مدرسهٔ متحده و فیوضات و متوسطهٔ تبریز و دارالفنون تهران گذراند و وارد دانشکدهٔ طب شد. سرگذشت عشق آتشین و ناکام او که به ترک تحصیل وی از رشتهٔ پزشکی در سال آخر منجر شد، مسیر زندگی او را عوض کرد و تحولات درونی او را به اوج معنوی ویژه‌ای کشانید و به اشعارش شور و حالی دیگر بخشید. وی سرانجام پس از هشتاد و سه سال زندگی شاعرانهٔ پربار در ۲۷ شهریور ماه ۱۳۶۷ هجری شمسی درگذشت و بنا به وصیت خود در مقبرة الشعرای تبریز به خاک سپرده شد.
